package com.sarath.methodoverloading;

public class Rectangle {

  private int with;
  private int breadth;

  public int getWith() {
    return with;
  }

  public void setWith(int with) {
    this.with = with;
  }

  public int getBreadth() {
    return breadth;
  }

  public void setBreadth(int breadth) {
    this.breadth = breadth;
  }
}
