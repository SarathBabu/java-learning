package com.sarath.pojo;

public class Rectangle {

  private final int with;
  private final int breadth;

  public Rectangle(int with, int breadth) {
    this.with = with;
    this.breadth = breadth;
  }


  public int getWith() {
    return with;
  }

  public int getBreadth() {
    return breadth;
  }

}
